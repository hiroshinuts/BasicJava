package estudando.java.se;

public class OperadoresDeIncremento {

	public static void main(String[] args) {
		
		int i = 0;
		int y = 0;
		
		// ++i Operador de pre-incremento - o valor será incrimentado antes da instrução, onde a variavel estiver contida
		
		i += 1; // ++i ; Pre Incremento
		System.out.println( i);
		
			
		
		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
		
		// i++ O valorsera incrementado apos a execucao onde a nossa expressaoe estiver contida
		
		
		System.out.println(y);
		i+= 1; // i++; Pos Incremento
		
		
		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
		
		System.out.println( "----------------------------------------------------------");
		
		//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
		
		int a = 0;
		int b = 0;
		
		System.out.println(--a); // PRE-INCREMENTO
		
		System.out.println(b--); // POS-INCREMENTO
		
		
		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
		System.out.println( "----------------------------------------------------------");
		
		////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
		
		
		//EXEMPLOS
		
		int q = 0;
		int w = 0;
		int e = 0;
		
		//FORMA TRADICIONAL // q = u + 10
		
		System.out.println(q = q + 1);
		
		// FORMA OPERADOR DE ATRIBUICAO // w += 1;
		
		System.out.println(w += 1);
		
		// FORMA OPERADOR INCREMENTO// e++
		
		System.out.println(e++);
		System.out.println(e++);
		System.out.println(e++);
		System.out.println(e++);
		System.out.println(e++);
		System.out.println(e++);
		
		
		
		
		
		
	}
	
}

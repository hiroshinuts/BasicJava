package estudando.java.se;

public class DoWhile {
	
	public static void main(String[] args) {
		//LAÇO DE REPETICAO DO/WHILE
		
		int i = 0; // declara i  = 0
		
		do{
			System.out.println(i); // imprime i , no caso seria 0
			i++; // incrementa o 0 + 1 e repete o DO até a condição do while ser verdadeira
			
		}while(i <= 10);
			
		
	}

}
